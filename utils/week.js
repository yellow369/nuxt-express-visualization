function week(params) {
  switch (params) {
    case 'Monday':
      return '星期一'
      break;
      case 'Tuesday':
        return '星期二'
        break;
        case 'Wednesday':
      return '星期三'
      break;
      case 'Thursday':
      return '星期四'
      break;
      case 'Friday':
      return '星期五'
      break;
      case 'Saturday':
      return '星期六'
      break;
      case 'Sunday':
      return '星期天'
      break;

  }
}

export default week